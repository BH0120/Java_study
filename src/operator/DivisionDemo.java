package operator;

public class DivisionDemo {
    public static void main(String[] args) {
        int a = 10;
        int b = 3;

        float c = 10.0f;
        float d = 3.0f;

        System.out.println(a / b);
        System.out.println(c / d);
        System.out.println(a / d);

        /*
        * 3
        * 3.3333333
        * 3.3333333
        * */
    }
}
